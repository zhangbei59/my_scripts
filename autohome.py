#获得最新云搭理源码

def get_soup(url):
    header = {'User-Agent': random.choice(user_agent_list)}
    req = request.Request(url, headers=header)
    response1 = request.urlopen(req)
    html_doc = response1.read()
    html_doc = BeautifulSoup(html_doc, 'lxml')
    return html_doc
    # print(type(html_doc))


