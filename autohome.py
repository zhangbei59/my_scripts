# 使用bs4获取代理地址，写入proxyes字典
def fecth_proxy_from_ip3336():
    proxyes = []
    url = 'http://www.ip3366.net/free/?stype=1&page='
    try:
        for i in range(0, 1):
            soup = get_soup(url + str(i))
            # print(type(soup))
            trs = soup.find_all("tr")
            # print(trs)
            for i, tr in enumerate(trs):
                if 0 == i:
                    continue
                tds = tr.find_all("td")
                # print(tds)
                ip = tds[0].string.strip()
                port = tds[1].string.strip()
                proxy = ''.join([ip, ':', port])#组合代理地址
                proxyes.append(proxy)
    except Exception as e:
        print('Failed to fecth_proxy_from_ip3336. Exception[%s]', e)
    return(proxyes)
    #print(proxyes)


